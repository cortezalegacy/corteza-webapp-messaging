<template>
  <lightbox>
      <header>
          <span class="closer" @click="onClose">&times;</span>
      </header>
      <main class="quick-search">
          <search-input focus="true" v-model="query"
          :placeholder="$t('search.quick.placeholder')"  />
          <ol>
              <li v-if="query && filtered.length===0" class="no-results">
                {{ $t('search.noMatchesFound') }}
              </li>
              <li v-for="i in (query ? filtered : preferred).slice(0, 10)"
                  @click="onClose"
                  :key="i.ID">
                <component :is="i.cmp" :ID="i.ID" :canCreate="canCreate" ></component>
              </li>
          </ol>
      </main>
      <i18next path="search.quick.footnote" tag="footer">
        <kbd>
          {{ $t('search.quick.shortcut') }}
        </kbd>
      </i18next>
  </lightbox>
</template>

<script>
import { mapGetters } from 'vuex'
import Lightbox from 'corteza-webapp-messaging/src/components/Lightboxed/index.vue'
import SearchInput from 'corteza-webapp-messaging/src/components/SearchInput'
import emitCloseOnEscape from 'corteza-webapp-messaging/src/mixins/emitCloseOnEscape'
import labelsMixin from 'corteza-webapp-messaging/src/mixins/labels'

const cmp = (type) => (i) => {
  i.cmp = `${type}-link`
  return i
}

export default {
  components: {
    SearchInput,
    Lightbox,
  },

  mixins: [
    emitCloseOnEscape,
    labelsMixin,
  ],

  data () {
    return {
      query: '',
      canCreate: false,
    }
  },

  computed: {
    ...mapGetters({
      channels: 'channels/list',
      users: 'users/list',
    }),

    filtered () {
      const q = this.query.toLocaleLowerCase()
      return this.targetNames(this.channelsAndUsers).filter(c => c.name.toLocaleLowerCase().indexOf(q) > -1)
    },

    // List of preferred channels -- ones we're not members of
    preferred () {
      return this.targetNames(this.preferredChannels)
    },

    preferredChannels () {
      return this.channels.filter(c => !c.isDirectMessage() && !c.isMember(this.$auth.user.userID)).map(cmp('channel'))
    },

    channelsAndUsers () {
      return [
        ...this.users.filter(u => u.userID !== this.$auth.user.userID).map(cmp('user')),
        ...this.channels.filter(c => !c.isDirectMessage()).map(cmp('channel')),
      ]
    },
  },

  created () {
    this.$MessagingAPI.permissionsEffective().then(e => {
      const canCreate = e.find(p => p.operation === 'channel.private.create')
      if (canCreate) {
        this.canCreate = canCreate.allow
      }
    })
  },

  methods: {
    onClose () {
      this.$emit('close')
    },

    onSearchSubmit ({ query }) {
      this.search(query)
    },

    targetNames (target) {
      return target.map(i => ({
        ID: i.channelID || i.userID,
        name: this.label(i),
        cmp: i.cmp,
      }))
    },
  },
}
</script>
